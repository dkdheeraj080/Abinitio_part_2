package com.example.dheeraj.abinitio_part_2;

class UserProfile {
    public String name;

    public String email;
    public String department;
    public String event;


    public UserProfile(String name, String email, String department, String event) {
        this.name = name;
        this.email = email;
        this.department = department;
        this.event = event;
    }
}
